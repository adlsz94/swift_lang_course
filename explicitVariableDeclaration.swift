// No tipo Explicito, você precisará especificar o tipo explicitamente

// Int explicitamente
var meuIntExplicito: Int = 17
print(meuIntExplicito)

// String explicitamente
var meuStringExplicito: String = "Isso é uma string explicita"
print(meuStringExplicito)

// Float explicitamente
var meuFloatExplicito: Float = 162.50
print(meuFloatExplicito)

// Double explicitamente
var meuDoubleExplicito: Double = 3.14159
print(meuDoubleExplicito)

// Bool explicitamente
var meuBoolExplicito: Bool = true
print(meuBoolExplicito)